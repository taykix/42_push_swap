#include "libft.h"

int	ft_isalnum(int str)
{
	if ((str >= 'a' && str <= 'z') || (str >= 'A' && str <= 'Z'))
		return (1);
	else if ((str >= '0' && str <= '9'))
		return (1);
	else
		return (0);
}
